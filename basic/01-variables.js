// variables

// var - (primera forma para declarar) (muy muy raro de usar)

var helloWorld = 'Hello World!';
console.log(helloWorld);

helloWorld = '¡Hola Mundo!';
console.log(helloWorld);

// let - (usar este para las variables normales)

let helloWorld2 = 'Hello World!';
console.log(helloWorld2);

helloWorld2 = '¡Hola Mundo!';
console.log(helloWorld2);

// const - (No se puede reasignar el valor - constante)

const helloWorld3 = 'Hello World!';
console.log(helloWorld3);
